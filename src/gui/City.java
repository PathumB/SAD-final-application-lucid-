    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String c = jTextField1.getText();

        if (c.isEmpty()) {
            jLabel3.setText("City Name *");
            jLabel3.setFont(new java.awt.Font("Segoe UI", 1, 13));
            jLabel3.setForeground(Color.red);
        } else {

            try {

                ResultSet rs1 = MySql.search("SELECT `city`.`name` FROM `city` WHERE `city`.`name` = '" + c + "';");

                if (rs1.next()) {
                    alerts.Alert_warning al = new alerts.Alert_warning(null, true);
                    al.alertMessage.setText("City Already Exist");
                    al.setVisible(true);
                } else {

                    jTextField1.setText("");
                    MySql.iud("INSERT INTO city (`name`) VALUES ('" + c + "');");
                    alerts.Alert_success a2 = new alerts.Alert_success(null, true);
                    a2.alertMessage.setText("City Added");
                    a2.setVisible(true);
                    
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

